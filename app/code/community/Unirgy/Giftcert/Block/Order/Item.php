class Unirgy_Giftcert_Block_Order_Item extends Mage_Sales_Block_Order_Item_Renderer_Default
{
    public function getItemOptions()
    {
        $result = parent::getItemOptions();
        Mage::helper('ugiftcert')->addOrderItemCertOptions($result, $this->getOrderItem());
        return $result;
    }
}
